// 스킬 검색 함수
export function getWowDBWarlockSkill(skillId) {
  const categories = Object.keys(wowdbWarlockSkillsComplete);

  for (const category of categories) {
    if (wowdbWarlockSkillsComplete[category] && wowdbWarlockSkillsComplete[category][skillId]) {
      return {
        ...wowdbWarlockSkillsComplete[category][skillId],
        category,
        id: skillId
      };
    }
  }

  return null;
}