// 스킬 검색 함수
export function getWowDBDruidSkill(skillId) {
  const categories = Object.keys(wowdbDruidSkillsComplete);

  for (const category of categories) {
    if (wowdbDruidSkillsComplete[category] && wowdbDruidSkillsComplete[category][skillId]) {
      return {
        ...wowdbDruidSkillsComplete[category][skillId],
        category,
        id: skillId
      };
    }
  }

  return null;
}