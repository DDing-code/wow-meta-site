// 헬퍼 함수들
export function getWowDBDemonHunterSkill(skillId) {
  const allCategories = [
    'baseline',
    'vengeance',
    'havoc',
    'talents',
    'pvp'
  ];

  for (const category of allCategories) {
    if (wowdbDemonHunterSkillsComplete[category] && wowdbDemonHunterSkillsComplete[category][skillId]) {
      return {
        ...wowdbDemonHunterSkillsComplete[category][skillId],
        category: category,
        id: skillId
      };
    }
  }
  return null;
}

export function getDemonHunterSkillCount() {
  let total = 0;
  const categories = Object.keys(wowdbDemonHunterSkillsComplete);

  for (const category of categories) {
    total += Object.keys(wowdbDemonHunterSkillsComplete[category]).length;
  }

  return {
    total,
    baseline: Object.keys(wowdbDemonHunterSkillsComplete.baseline).length,
    vengeance: Object.keys(wowdbDemonHunterSkillsComplete.vengeance).length,
    havoc: Object.keys(wowdbDemonHunterSkillsComplete.havoc).length,
    talents: Object.keys(wowdbDemonHunterSkillsComplete.talents).length,
    pvp: Object.keys(wowdbDemonHunterSkillsComplete.pvp).length
  };
}

export function getDemonHunterSkillsBySpec(spec) {
  const specMap = {
    'vengeance': 'vengeance',
    'havoc': 'havoc'
  };

  const category = specMap[spec?.toLowerCase()];
  if (!category) return null;

  return {
    baseline: wowdbDemonHunterSkillsComplete.baseline,
    specialization: wowdbDemonHunterSkillsComplete[category],
    talents: wowdbDemonHunterSkillsComplete.talents,
    pvp: wowdbDemonHunterSkillsComplete.pvp
  };
}