// 스킬 검색 함수
export function getWowDBMageSkill(skillId) {
  const categories = Object.keys(wowdbMageSkillsComplete);

  for (const category of categories) {
    if (wowdbMageSkillsComplete[category] && wowdbMageSkillsComplete[category][skillId]) {
      return {
        ...wowdbMageSkillsComplete[category][skillId],
        category,
        id: skillId
      };
    }
  }

  return null;
}