// 스킬 검색 함수
export function getWowDBShamanSkill(skillId) {
  const categories = Object.keys(wowdbShamanSkillsComplete);

  for (const category of categories) {
    if (wowdbShamanSkillsComplete[category] && wowdbShamanSkillsComplete[category][skillId]) {
      return {
        ...wowdbShamanSkillsComplete[category][skillId],
        category,
        id: skillId
      };
    }
  }

  return null;
}