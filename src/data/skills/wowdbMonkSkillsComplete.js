// 스킬 검색 함수
export function getWowDBMonkSkill(skillId) {
  const categories = Object.keys(wowdbMonkSkillsComplete);

  for (const category of categories) {
    if (wowdbMonkSkillsComplete[category] && wowdbMonkSkillsComplete[category][skillId]) {
      return {
        ...wowdbMonkSkillsComplete[category][skillId],
        category,
        id: skillId
      };
    }
  }

  return null;
}